"""
+ - Adição
- - Menos
* - Multiplicação
/ - Divisão
//- Divisão inteira
** - exponensiação
% - Mod, que nem em C, mostra o resto da divisão
()- Muda a ordem de precedência
"""

print('Adição:', 10+10)
print('Subtração:', 10-10)
print('Multiplicação:', 10*10)
print('Divisão:', 10/10)
print('Divisão inteira:', 10 // 3)
print('Exponensiação:', 10**10)
print('Mod:', 2 % 10)
print('Conta com parênteses:', (10+2)*2)
